    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        
        df = (DefaultTableModel)jTable1.getModel();
        int selected = jTable1.getSelectedRow();
        
        
        
        txtfname.setText((df.getValueAt(selected, 0).toString()));
        txtlname.setText((df.getValueAt(selected, 1).toString()));
        txtphn.setText((df.getValueAt(selected, 2).toString()));
        txtmail.setText((df.getValueAt(selected, 3).toString()));
        txtpass.setText((df.getValueAt(selected, 4).toString()));
        String rbutton = (df.getValueAt(selected, 5).toString());
        if (rbutton.equals("Admin")){
            rbadmin.setSelected(true);
        }
        else {
            rbuser.setSelected(true);
        }
        txtrecover.setText((df.getValueAt(selected, 6).toString()));
        jButton3.setEnabled(false);
        
        
        
        
    }//GEN-LAST:event_jTable1MouseClicked

