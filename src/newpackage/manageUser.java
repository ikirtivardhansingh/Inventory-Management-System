    public void load(){
        int a;
        try {
            
            pst = con.prepareStatement("select * from logindetails");
            ResultSet rs = pst.executeQuery();
            ResultSetMetaData rd = rs.getMetaData();
            a = rd.getColumnCount();
            df = (DefaultTableModel)jTable1.getModel();
            df.setRowCount(0);
            
            while(rs.next()){
                Vector v2 = new Vector();
                for(int i=1;i<=a;i++){
                    v2.add(rs.getString("Fname"));
                    v2.add(rs.getString("Lname"));
                    v2.add(rs.getString("Phone"));
                    v2.add(rs.getString("Email"));
                    v2.add(rs.getString("Password"));
                    v2.add(rs.getString("usertype"));
                    v2.add(rs.getString("Ocode"));
                    
                }
                df.addRow(v2);
            }
            
            
            
            
        } catch (SQLException ex3) {
            JOptionPane.showMessageDialog(rootPane, ex3.getMessage());
            Logger.getLogger(manageUser.class.getName()).log(Level.SEVERE, null, ex3);
        }
    }

