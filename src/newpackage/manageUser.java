    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
        String fname = txtfname.getText();
        String lname = txtlname.getText();
        String Phn = txtphn.getText();
        String mail = txtmail.getText();
        String pass  = txtpass.getText();
        String Usert = "";
        String Rcode = txtrecover.getText();
        
        
        if (rbadmin.isSelected()){
        Usert = "Admin";
        }
        else if (rbuser.isSelected()) {
        Usert = "User";
        }
        else {
        JOptionPane.showMessageDialog(rootPane, "Plesase Select a User Type..", "User Type Error!!", 1);
        }
        
        if(fname.equals("") || lname.equals("") || Phn.equals("") || mail.equals("") || pass.equals("") || Rcode.equals("") ){
            JOptionPane.showMessageDialog(rootPane, "Plesase Fill all the Details!!..", "Error", 1);
        }
       
        else try {
            pst = con.prepareStatement("insert into logindetails(Fname,Lname,Phone,Email,Password,usertype,Ocode)values(?,?,?,?,?,?,?)");
            pst.setString(1,fname);
            pst.setString(2,lname);
            pst.setString(3,Phn);
            pst.setString(4,mail);
            pst.setString(5,pass);
            pst.setString(6,Usert);
            pst.setString(7,Rcode);
            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data Added Successfully");
            
            txtfname.setText("");
            txtlname.setText("");
            txtphn.setText("");
            txtmail.setText("");
            txtpass.setText("");
            txtrecover.setText("");
            rbuser.setSelected(false);
            rbadmin.setSelected(false);
            load();
          
            
            
        } catch (SQLException ex2) {
            JOptionPane.showMessageDialog(rootPane, ex2.getMessage());
            Logger.getLogger(manageUser.class.getName()).log(Level.SEVERE, null, ex2);
        }
        
   
    }//GEN-LAST:event_jButton3ActionPerformed

